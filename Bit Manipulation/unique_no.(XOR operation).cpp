//using XOR Operator in which same will be zero and different will be 1
//condition : number must occur twice or once if it occur three times(odd) then it will consider as new and print as unique
//doesn't use extra space
#include<iostream>
using namespace std;

int main(){
    int n;
    cin>>n;
    int ans=0;
    int no;
    for(int i=0; i<n; i++){
        cin>>no;
        ans = ans^no;
    }
    cout<<ans<<endl;
    return 0;  
}